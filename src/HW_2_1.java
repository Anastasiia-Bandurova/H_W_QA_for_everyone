public class HW_2_1 {
    public static void main(String[] args) {
        int a = 7;
        int b = 2;
        int resultOfSum = a + b;
        System.out.println(resultOfSum);

        int resultOfMult = a * b;
        System.out.println(resultOfMult);

        int resultOfSubstraction = a - b;
        System.out.println(resultOfSubstraction);

        double resultOfDevision = (double) a / b;
        System.out.println(resultOfDevision);

        boolean isOdd = (a % 2) == 1;
        System.out.println(isOdd);

        boolean isEven = (b % 2) == 0;
        System.out.println(isEven);
    }
}
